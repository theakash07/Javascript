// Prompt the user to enter a value for 'n' and store the input in the variable 'n'.
let n = prompt('Enter the value of n:\n');

// Initialize a variable 'i' to serve as a counter.
let i = 0;

// Use a 'do...while' loop to repeatedly execute the code block.
do {
    // Print the current value of 'i' to the console.
    console.log(i);

    // Increment the value of 'i' by 1.
    i++;
} while (i < n);
