// Prompt the user to enter a value for 'a' and store the input in the variable 'a'.
let a = prompt("Enter value of a:\n");

// Initialize a variable 'i' to 0 to serve as a counter.
let i = 0;

// Use a 'while' loop to iterate as long as 'i' is less than 'a'.
while (i < a) {
    // Print the current value of 'i' incremented by 1 to the console.
    console.log(i + 1);

    // Increment the value of 'i' by 1.
    i++;
}
